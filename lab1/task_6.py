R = 8.314

p = float(input("Введите давление газа в Паскалях (Па): "))
V = float(input("Введите объем газа в кубических метрах (м^3): "))
T_celsius = float(input("Введите температуру газа в градусах Цельсия (°C): "))

T = T_celsius + 273.15

n = (p * V) / (R * T)

print(f"Количество вещества газа: {n} моль")